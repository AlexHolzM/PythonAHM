'''
#*                     Operadores Aritméticos:

Los operadores aritméticos se utilizan para realizar cálculos matemáticos en Python. Aquí están los operadores básicos:

    + (Suma): Suma dos valores.
    - (Resta): Resta el segundo valor del primero.
    * (Multiplicación): Multiplica dos valores.
    / (División): Divide el primer valor por el segundo.
    // (División entera): Realiza una división entera, descartando el residuo.
    % (Módulo): Devuelve el residuo de la división.

Ejemplo de operadores aritméticos:
'''
# Ejemplo de suma
a = 10
b = 5
resultado = a + b
print(resultado)  # Resultado: 15
# __________________________________

'''
#*                      Operadores de Cadena:

Los operadores de cadena se utilizan para manipular texto en Python.

    + (Concatenación): Une dos cadenas de texto.
    * (Repetición): Repite una cadena de texto varias veces.

Ejemplo de operadores de cadena:
'''
# Ejemplo de concatenación
nombre = "Juan"
apellido = "Pérez"
nombre_completo = nombre + " " + apellido
print(nombre_completo)  # Resultado: "Juan Pérez"

# Ejemplo de repetición
saludo = "Hola, "
repeticion = saludo * 3
print(repeticion)  # Resultado: "Hola, Hola, Hola, "
