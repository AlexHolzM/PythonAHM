
# * Variables *

nombre = 'alex'  # str
print(nombre)

edad = 30  # int
print(edad)

e_bool = False
print(e_bool)  # bool

# * Concatenacion
print(nombre, edad, e_bool)

# * variable entera a caracter
my_int = 5
print(my_int)

my_int_to_str = str(my_int)
print(my_int_to_str)
print(type(my_int_to_str))

# * Funciondes del sistema *

print(len(nombre))  # contar caracteres

